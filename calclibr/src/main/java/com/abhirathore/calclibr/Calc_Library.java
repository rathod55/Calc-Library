package com.abhirathore.calclibr;

public class Calc_Library {

    public static float add(float num1, float num2)
    {
        float result_add=num1+num2;
        return result_add;
    }
    public static float sub(float num1,float num2)
    {
        float result_sub=num1-num2;
        return result_sub;
    }
    public static float mul(float num1, float num2 )
    {
        float result_mul=num1 * num2;
        return result_mul;
    }
    public static float div(float num1,float num2)
    {
        float result_div=num1/num2;
        return result_div;
    }

    public static float mod (float num1,float num2)
    {
        float result_mod=num1%num2;
        return result_mod;
    }
}
